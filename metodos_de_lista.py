idades = [23, 24, 25, 26]
idades_final = [27, 28]

# Insert, inseri dados na posicação descrita, insert(posição, valor)
idades.insert(0, 56)
# Remove, remove o valor descrito dentro da lsita, remove (valor_a_ser_removido)
idades.remove(23)
# Pop, remove indicando a posicação do item, pop(posição_a_ser_removido)
idades.pop(23)
# clear, remove toda lista, clear()
idades.clear()
# Append, adicona o elementro descrito na lista, append(valor_a_ser_adicionado)
idades.append(29)
# Count, irá contar quantas vezes se repete o valor descrito, count(valor_a_ser_contado)
idades.count(26)
# index, retornará a posicaão do item descrito, pop(valor_a_ser_consultado_o_index)
idades.index(26)
# copy, simplesmente copia a lista e retorna uma lista superficial com os valores.
idades.copy()
# extend, faz a junção de duas listas e retorna os valores, extend(valor_da_outra_lista)
idades.extend(idades_final)
# reverse, inverte os valores ex: 1, 2 | reverse -> 2, 1.
idades.reverse()
# sort, classifica a lista em ordem crescente ou decrescente, com TRUE OU FALSE.
idades.sort(reverse=False)
# sort, classifica a lista em ordem crescente ou decrescente, com TRUE OU FALSE.

